export function searchIcd(query: string): IcdCode[] {
  if (!query || query.length < 1) return [];
  const q = query.toLowerCase();
  return ICD10_LIST.filter(
    (icd) =>
      icd.code.toLowerCase().includes(q) ||
      icd.name.toLowerCase().includes(q),
  ).slice(0, 12);
}
